/**
 * 所有朝代绘画信息的列表
 */
export const dynastyPaintingInfoList: DynastyPaintingInfo[] = [
  tangPainting,    // 唐代
  songPainting,    // 宋代
  yuanPainting,    // 元代
  mingPainting,    // 明代
  qingPainting     // 清代
];

/**
 * 根据朝代ID获取朝代绘画信息
 * @param id 朝代ID
 * @returns 对应的朝代绘画信息对象，如果未找到则返回undefined
 */
export function getDynastyPaintingInfoById(id: number): DynastyPaintingInfo | undefined {
  return dynastyPaintingInfoList.find(info => info.id === id);
}

/**
 * 根据朝代名称获取朝代绘画信息
 * @param name 朝代名称
 * @returns 对应的朝代绘画信息对象，如果未找到则返回undefined
 */
export function getDynastyPaintingInfoByName(name: string): DynastyPaintingInfo | undefined {
  return dynastyPaintingInfoList.find(info => info.name === name);
}

/**
 * 获取所有朝代名称
 * @returns 所有朝代名称的数组
 */
export function getAllDynastyNames(): string[] {
  return dynastyPaintingInfoList.map(info => info.name);
}

/**
 * 根据朝代ID获取其代表作品
 * @param dynastyId 朝代ID
 * @returns 该朝代的代表作品数组，如果未找到则返回空数组
 */
export function getMasterpiecesByDynastyId(dynastyId: number): PaintingInfo[] {
  const dynasty = getDynastyPaintingInfoById(dynastyId);
  return dynasty ? dynasty.masterpieces : [];
}

/**
 * 根据朝代ID获取其著名画家
 * @param dynastyId 朝代ID
 * @returns 该朝代的著名画家数组，如果未找到则返回空数组
 */
export function getFamousArtistsByDynastyId(dynastyId: number): Array<{name: string, lifetime: string, achievements: string, story?: string}> {
  const dynasty = getDynastyPaintingInfoById(dynastyId);
  return dynasty ? dynasty.famousArtists : [];
}